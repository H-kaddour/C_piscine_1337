#include <unistd.h>

void	ft_is_negative(int n)
{
	char	m;

	if (n >= 0)
	{	
		m = 'P';
		write(1, &m, 1);
	}
	else
	{
		m = 'N';
		write(1, &m, 1);
	}
}
