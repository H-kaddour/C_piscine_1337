int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 0 && str[i] <= 64)
		{
			return (0);
		}
		else if (str[i] >= 91 && str[i] <= 127)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
