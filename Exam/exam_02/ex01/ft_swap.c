#include <stdio.h>
void	ft_swap(int *a, int*b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}
int main()
{
	int a1;
	int b1;

	a1 = 3;
	b1 = 2;
	ft_swap(&a1, &b1);
	printf("a =%d, b=%d", a1, b1);
	return (0);
}
